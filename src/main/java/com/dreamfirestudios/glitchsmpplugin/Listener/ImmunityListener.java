package com.dreamfirestudios.glitchsmpplugin.Listener;

import com.dreamfirestudios.dreamcore.DreamJava.PulseAutoRegister;
import com.dreamfirestudios.glitchsmpplugin.GlitchSMPPlugin;
import org.bukkit.entity.Player;
import org.bukkit.event.EventHandler;
import org.bukkit.event.EventPriority;
import org.bukkit.event.Listener;
import org.bukkit.event.entity.EntityDamageEvent;

/**
 * <summary>
 * Immunity listener: cancels all incoming damage while active.
 * </summary>
 */
@PulseAutoRegister
public final class ImmunityListener implements Listener {

    /** <summary>Prevents damage while immunity is active.</summary> */
    @EventHandler(priority = EventPriority.HIGHEST, ignoreCancelled = true)
    public void onDamage(EntityDamageEvent e) {
        if (!(e.getEntity() instanceof Player p)) return;
        if (!GlitchSMPPlugin.GetImmunityGuard().isActive(p.getUniqueId())) return;
        e.setCancelled(true);
    }
}